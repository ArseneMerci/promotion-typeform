export default function printStepName(step) {
    switch (step) {
        case 1:
            return "My Project";
        case 2:
            return "My Decoration";
        case 3:
            return "My Request";
        case 4:
            return "Image Uploads";
        case 5:
            return "Information";
        default:
            return "My project";
    }
}
